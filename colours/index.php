            <h3>White or gold</h3>
            <p>Symbolism: celebration, exuberance, glory, innocence, joy, light, purity, resurrection, victory</p>
            <p><img src="../assets/img/damask-white.png" alt="white"></p>
            <p>White contains all the colours of the rainbow and is worn on major festivals. Vestments may be white, ivory, cream or silver, gold or yellow.</p>
            <p>As a colour for funerals, particularly of children and clergy, white points to the hope of Easter.</p>
            <p>When to use:</p>
            <ul>
                <li>Christmas <span class="rubric">(25 December to 5 January)</span></li>
                <li>Feasts of the Lord
                    <ul>
                        <li>The Naming of Jesus <span class="rubric">(1 January)</span></li>
                        <li>The Epiphany <span class="rubric">(6 January)</span></li>
                        <li>The Baptism of the Lord <span class="rubric">(First Sunday after Epiphany)</span></li>
                        <li>The Presentation of the Lord <span class="rubric">(2 February)</span></li>
                        <li>The Annunciation of the Lord <span class="rubric">(25 March)</span></li>
                        <li>Ascension Day <span class="rubric">(Thursday after the Sixth Sunday of Easter)</span></li>
                        <li>The Transfiguration of the Lord <span class="rubric">(6 August)</span></li>
                        <li>The Feast of Christ the King <span class="rubric">(The Last Sunday after Pentecost, that is the Sunday before Advent)</span></li>
                    </ul>
                </li>
                <li>Maundy Thursday <span class="rubric">(Eucharist of the Lord's Supper)</span></li>
                <li>Easter <span class="rubric">(from Easter Vigil to the Saturday after the Seventh Sunday of Easter)</span></li>
                <li>Trinity Sunday <span class="rubric">(Trinity Sunday to the Saturday after Trinity Sunday)</span></li>
                <li>Corpus Christi <span class="rubric">(Thanksgiving for Holy Communion, Thursday after Trinity Sunday)</span></li>
                <li>Holy Cross Day <span class="rubric">(14 September)</span></li>
                <li>Feasts of St Mary the Virgin
                    <ul>
