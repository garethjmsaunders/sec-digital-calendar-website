                        <li>The Conception of St Mary <span class="rubric">(8 December)</span></li>
                    </ul>
                </li>
                <li>Commemoration of saints <span class="rubric">(those who were not martyred, including St John the Evangelist)</span></li>
                <li>All Saints' Day <span class="rubric">(1 November)</span></li>
                <li>All Souls' Day <span class="rubric">(2 November, if Black or Violet are not used)</span></li>
