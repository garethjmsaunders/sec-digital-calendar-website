            <h3 id="class-5">Class 5</h3>

            <p>Class 5 includes observances that are appointed in the calendar but are not required for observance throughout the whole Church. These days may be kept where there is local custom, pastoral reason or particular devotion, but they stand below classes 1 to 4 in the order of precedence.</p>

