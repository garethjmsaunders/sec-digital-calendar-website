              <li>Trinity Sunday</li>
              <li>Eve of Pentecost</li>
              <li>The Transfiguration of the Lord</li>
              <li>All Saints</li>
            </ul>

            <h3 id="class-3">Class 3</h3>

            <p>Class 3 includes the regular seasonal Sundays that shape much of the Church year outside the principal feasts and festivals. These Sundays are part of the ordinary rhythm of worship, but they still have a defined place in the order of precedence. </p>

            <p><i>Arranged in calendar order</i></p>

            <ul>
              <li>Second Sunday after Christmas</li>
              <li>Sundays after Epiphany, from the Second Sunday onwards</li>
              <li>Sunday before Lent</li>
              <li>Sundays after Pentecost, from the Second Sunday onwards</li>
              <li>The Last Sunday after Pentecost, Christ the King</li>
            </ul>

