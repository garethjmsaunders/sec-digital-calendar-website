            <h3 id="class-2">Class 2</h3>

            <p>Class 2 includes major feasts and Sundays that are widely observed across the Church, but which stand below the principal days of class 1 in the order of precedence. These are important celebrations in the Christian year, marking key moments in the life of Christ and the Church. </p>

            <p><i>Arranged in calendar order</i></p>

