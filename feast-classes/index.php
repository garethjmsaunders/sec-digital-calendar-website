            <h1>Feast classes</h1>
            <p class="lead">A brief guide to feast classes used in the calendar of the Scottish Episcopal Church.</p>
        
            <p>This page offers a brief guide to those classes and to the ordinary weekdays known as feria. Together, they help explain why some days always shape the Church's worship, while others may be kept more simply or optionally depending on the season, the local church and the calendar date.</p>

            <hr>

            <h2>What are feast classes?</h2>

            <p>Feast classes are the way the Scottish Episcopal Church orders the celebrations in its calendar.</p>
            <p>They are not simply a list of 'more important' and 'less important' days. Rather, they show precedence: in other words, which observance takes priority when two fall on the same date.</p>
            <p>The SEC Calendar and Lectionary explains that commemorations are classified into six groups, and that 'the lower number takes precedence'.</p>
            <p>It also notes that categories 1 to 4 are intended for observance by the whole Church, while categories 5 and 6 are optional.</p>
            
            <hr>

