            <h3 id="feria">Feria (ordinary weekday)</h3>

            <p>In church calendar language, a 'feria' is an ordinary weekday, meaning a day that is not being kept as a feast or commemoration of a particular class. In Anglican usage, a ferial day is essentially the default weekday observance of the season rather than a named saint's day or festival.</p>

            <p>In the Scottish Episcopal Church, that means the ordinary weekdays that fall between the more clearly ranked observances in classes 1 to 6.</p>

            <p>So, for example, many Mondays to Saturdays through the year are feria, unless they are taken up by a feast, a commemoration, or one of the specially weighted weekdays such as those in Lent, which the SEC places in class 4.</p>

            <p>Examples:</p>

            <ul>
              <li>Monday in Easter week</li>
              <li>Tuesday after Fourth Sunday of Advent</li>
              <li>Wednesday after the Epiphany</li>
              <li>Thursday after Pentecost 7</li>
              <li>Friday after Pentecost 12</li>
              <li>Saturday after the First Sunday after Christmas</li>
            </ul>

