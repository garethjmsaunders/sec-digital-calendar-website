            <h1>Browser toolbar extensions</h1>
            <p class="lead">View the Scottish Episcopal Church digital calendar and lectionary in a popup window on your web browser's toolbar.</p>
            
            <p><img src="../assets/img/google-chrome-ext-screenshot.jpg" alt="Screenshot of popup window showing SEC Digital Calendar" width="100%"></p>

            <p>Save time with this handy browser extension to quickly view today's feast in a popup window on your browser's toolbar.</p>

            <p>The button opens the full website in mobile-view in a small window, so you can access everything on this site within the toolbar popup.</p>

            <p>Want to move everything to a full tab? Right-click a link and select 'Open link in new tab'.</p>

