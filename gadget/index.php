            <h1>Windows Sidebar Gadget beta</h1>
            <p class="lead">View the Scottish Episcopal Church digital calendar and lectionary in a Windows Sidebar Gadget.</p>
            
            <p><img src="../assets/gadget/screenshot.png" alt="Screenshot of SEC Digital Calendar Windows Sidebar Gadget"></p>

            <div class="alert alert-dark alert-sec text-center damask-red">âš  WARNING This Windows Sidebar Gadget is still in beta. It may still need some cosmetic tweaks.</div>

            <p>Windows Vista and Windows 7 supported Sidebar Gadgets, mini applications that sat on the desktop or on a sidebar. These are still available in Windows 7, 8, 8.1, 10 and 11 by installing the application <a href="https://8gadgetpack.net/">8GadgetPack</a>.<p>

            <h3>How it works</h3>
            <p>The gadget displays the latest information from the SEC digital calendar website.</p>

            <p>Long feast day names and descriptions will be truncated. This is expected behaviour.</p>

            <p>Hover over the feast day to view more information in a tooltip. This will show:</p>

            <ul>
                <li>Date</li>
                <li>Season</li>
                <li>Feast day</li>
                <li>Feast class (1 to 6)</li>
                <li>Liturgical colour</li>
                <li>Translated</li>
                <li>Ember or rogation day</li>
            </ul>
            
            <p>Currently, the gadget does not automatically update after midnight. To manually refresh the gadget, click the SEC logo in the top-right corner.</p>

            <h3>Installation</h3>
            <ol>
                <li>Download and install <a href="https://8gadgetpack.net/">8GadgetPack</a>.</li>
                <li>Download <a href="https://github.com/garethjmsaunders/sec-digital-calendar-windows-sidebar-gadget/blob/main/SECdigitalCalendar-v1.0.0.gadget?raw=true">SECdigitalCalendar-v1.0.0.gadget</a> from this repository and double-click the icon to install.</li>
                <li>Right-click the desktop and select Gadgets.</li>
                <li>Navigate to the SEC digital calendar gadget and either right-click and select Add or drag-and-drop the gadget onto your desktop.</li>
            </ol>
                        
